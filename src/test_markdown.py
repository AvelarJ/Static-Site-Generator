import unittest

from markdown_utilities import markdown_to_blocks, split_nodes_delimiter, extract_markdown_images, extract_markdown_links, split_nodes_image, split_nodes_link, text_to_textnodes
from textnode import TextNode, TextType

class TestMarkdown(unittest.TestCase):
    
    def test_one_code(self):
        node = TextNode("This is text with a `code block` word", TextType.TEXT)
        new_nodes = split_nodes_delimiter([node], "`", TextType.CODE)
        self.assertEqual(new_nodes, [TextNode("This is text with a ", TextType.TEXT),TextNode("code block", TextType.CODE),TextNode(" word", TextType.TEXT),])
        
    def test_two_code(self):
        node = TextNode("This is `text` with 2 `code block` words", TextType.TEXT)
        new_nodes = split_nodes_delimiter([node], "`", TextType.CODE)
        self.assertEqual(new_nodes, [TextNode("This is ", TextType.TEXT),TextNode("text", TextType.CODE),TextNode(" with 2 ", TextType.TEXT),TextNode("code block", TextType.CODE),TextNode(" words", TextType.TEXT),])

    def test_mix_code_bold(self):
        node = TextNode("This is text with a `code block` word and a **bold** word", TextType.TEXT)
        new_nodes = split_nodes_delimiter([node], "`", TextType.CODE)
        self.assertEqual(new_nodes, [TextNode("This is text with a ", TextType.TEXT),TextNode("code block", TextType.CODE),TextNode(" word and a **bold** word", TextType.TEXT),])

    def test_one_code_first(self):
        node = TextNode("`This` is text with a code block word", TextType.TEXT)
        new_nodes = split_nodes_delimiter([node], "`", TextType.CODE)
        self.assertEqual(new_nodes, [TextNode("This", TextType.CODE),TextNode(" is text with a code block word", TextType.TEXT)])
    
